def mux_scene(scene_id, step, vid_path, aud_path, out_path):
    cmd = [
        "ffmpeg", "-y", "-v", "error",
        "-i", str(vid_path),
        "-i", str(aud_path),
        "-c:v", "copy",
        "-c:a", "aac",
        "-b:a", "192k",
        "-af", "loudnorm=I=-16:TP=-1.5:LRA=11",
        "-map", "0:v:0",
        "-map", "1:a:0",
        "-shortest",
        str(out_path)
    ]
    try:
        subprocess.check_call(cmd)
        return True
    except subprocess.CalledProcessError as e:
        print(f"      ❌ Mux failed for scene {scene_id:02d}: {e}")
        return False

def concat_scenes(concat_txt, out_path):
    cmd = [
        "ffmpeg", "-y", "-v", "error",
        "-f", "concat",
        "-safe", "0",
        "-i", str(concat_txt),
        "-c:v", "libx264",
        "-preset", "slow",
        "-crf", "18",
        "-c:a", "aac",
        "-b:a", "192k",
        "-movflags", "+faststart",
        "-pix_fmt", "yuv420p",
        str(out_path)
    ]
    try:
        subprocess.check_call(cmd)
        return True
    except subprocess.CalledProcessError as e:
        print(f"   ❌ Final concatenation failed: {e}")
        return False

